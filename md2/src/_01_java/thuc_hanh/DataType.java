package _01_java.thuc_hanh;

public class DataType {
    public static void main(String[] args) {
        int i = 10; // khai báo biến i có giá trị là 10 i là integer
        float f = 20.5f; //khai báo biến f có kiểu là float và giá trị là 20.5
        double d = 20.5; // khai báo biến d có kiểu là duoble có giá trị là 20.5
        boolean b = true; //khai báo biến b kiểu boolean có giá trị là true
        char c = 'a'; // khai báo biến c có giá trị là kí tự 'a'
        String s = "Hà Nội"; //
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
